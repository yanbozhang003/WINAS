function pe = CalculatePe(p, bValue)
    D = sqrt(4*p*(1-p));
    
    if bValue==1
        % coding rate = 1/2;
        pe = 0.5 * (36.0 * D^10 + ...
                    211.0 * D^12 + ...
                    1404.0 * D^14 + ...
                    11633.0 * D^16 + ...
                    77433.0 * D^18 + ...
                    502690.0 * D^20 + ...
                    3322763.0 * D^22 + ...
                    21292910.0 * D^24 + ...
                    134365911.0 * D^26);
    end
    
    if bValue==2
        % coding rate = 2/3
        pe = 1.0 / (2.0 * bValue) * ( 3.0 * D^6 + ...
                                      70.0 * D^7 + ...
                                      285.0 * D^8 + ...
                                      1276.0 * D^9 + ...
                                      6160.0 * D^10 + ...
                                      27128.0 * D^11 + ...
                                      117019.0 * D^12 + ...
                                      498860.0 * D^13 + ...
                                      2103891.0 * D^14 + ...
                                      8784123.0 * D^15);
    end
    
    if bValue==3
        % coding rate = 3/4
        pe = 1.0 / (2.0 * bValue) * ( 42.0 * D^5 + ...
                                      201.0 * D^6 + ...
                                      1492.0 * D^7 + ...
                                      10469.0 * D^8 + ...
                                      62935.0 * D^9 + ...
                                      379644.0 * D^10 + ...
                                      2253373.0 * D^11 + ...
                                      13073811.0 * D^12 + ...
                                      75152755.0 * D^13 + ...
                                      428005675.0 * D^14);
    end
    
    if bValue==5
        % coding rate = 5/6
        pe = 1.0 / (2.0 * bValue) * ( 92.0 * D^4.0 + ...
                                      528.0 * D^5.0 + ...
                                      8694.0 * D^6.0 + ...
                                      79453.0 * D^7.0 + ...
                                      792114.0 * D^8.0 + ...
                                      7375573.0 * D^9.0 + ...
                                      67884974.0 * D^10.0 + ...
                                      610875423.0 * D^11.0 + ...
                                      5427275376.0 * D^12.0 + ...
                                      47664215639.0 * D^13.0);
    end
    
end

